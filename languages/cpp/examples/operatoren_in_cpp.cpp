#include <iostream>

int main() {
    int a = 10, b = 20;

    std::cout << "Addition: " << (a + b) << std::endl;
    std::cout << "Subtraktion: " << (a - b) << std::endl;
    std::cout << "Multiplikation: " << (a * b) << std::endl;
    std::cout << "Division: " << (b / a) << std::endl;
    std::cout << "Modulo: " << (b % a) << std::endl;

    return 0;
}
