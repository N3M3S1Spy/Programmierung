#include <iostream>

int main() {
    int number = 10;

    // if-else Beispiel
    if (number > 5) {
        std::cout << "Zahl ist größer als 5." << std::endl;
    } else {
        std::cout << "Zahl ist kleiner oder gleich 5." << std::endl;
    }

    // for-Schleife Beispiel
    for (int i = 0; i < 5; ++i) {
        std::cout << "i ist: " << i << std::endl;
    }

    return 0;
}
