#include <iostream>

int main() {
    int integerVar = 10;
    double doubleVar = 3.14;
    char charVar = 'A';
    bool boolVar = true;

    std::cout << "Integer: " << integerVar << std::endl;
    std::cout << "Double: " << doubleVar << std::endl;
    std::cout << "Char: " << charVar << std::endl;
    std::cout << "Boolean: " << boolVar << std::endl;

    return 0;
}
